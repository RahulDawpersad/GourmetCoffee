function filterMenu() {
    const coffeeType = document.getElementById("coffeeType").value
    const size = document.getElementById("size").value
    const priceRange = document.getElementById("priceRange").value

    let filteredMenu = menuItems

    if (coffeeType !== "all") {
        filteredMenu = filteredMenu.filter((item) => item.type === coffeeType)
    }

    if (size !== "all") {
        filteredMenu = filteredMenu.filter((item) => item.size === size)
    }

    if (priceRange !== "all") {
        if (priceRange === "low") {
            filteredMenu = filteredMenu.sort((a, b) => a.price - b.price)
        } else {
            filteredMenu = filteredMenu.sort((a, b) => b.price - a.price)
        }
    }

    renderMenu(filteredMenu)
}

function renderMenu(items) {
    const menuContainer = document.getElementById("menu");
    menuContainer.innerHTML = "";

    if (items.length === 0) {
        menuContainer.innerHTML = `<p>No items found based on your filter criteria.</p>`;
    }

    items.forEach((item) => {
        const menuItem = document.createElement("div");
        menuItem.classList.add("menu-item");

        const sizeLabel = item.size === "regular" ? "Regular" : "Large";

        menuItem.innerHTML = `
            <div class="menu-item-content">
                <img src="${item.image}" alt="${item.name}">
                <div class="item-info">
                    <h2>${item.name}</h2>
                    <p class="size">${sizeLabel}</p>
                    <p class="price">R${item.price}.00</p>
                </div>
            </div>
        `;
        menuContainer.appendChild(menuItem);
    });
}


